package lexical;

/**
 * The enum Types.
 */
public enum Types {
    /**
     * Lpa types.
     */
    LPA,    //left parenthesis
    /**
     * Rpn types.
     */
    RPN,    //right parenthesis
    /**
     * Str types.
     */
    STR,    //string
    /**
     * Par types.
     */
    PAR,    //parameter indicator
    /**
     * Com types.
     */
    COM,    //command
    /**
     * End types.
     */
    END,    //line end
    /**
     * Atm types.
     */
    ATM,    //atomic
}
